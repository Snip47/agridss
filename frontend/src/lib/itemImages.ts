// Helper: get image by name (fuzzy match)
export function getCropImage(name: string): string {
  const key = name.toLowerCase()
  for (const [k, v] of Object.entries(CROP_IMAGES)) {
    if (key.includes(k) || k.includes(key.split(' ')[0])) return v
  }
  return 'https://images.unsplash.com/photo-1574943320219-553eb213f72d?w=400&q=80'
}

